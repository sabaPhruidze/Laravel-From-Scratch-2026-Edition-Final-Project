    public function index(Request $request)
    {
        $user = $request->user();

        $ideas = $user
            ->ideas()
            ->when($request->filled('status'), fn ($query) => $query->where('status', $request->input('status')))
            ->get();

        return view('ideas.index', [
            'ideas' => $ideas,
            'statusCounts' => Idea::statusCounts(Auth::user()),
